package Oct.ex_21102024_loops;

public class Lab032 {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {

            // Check if the number is even or odd
            if (i % 2 == 0)
            {
                System.out.println(i + " is an even number.");
            }
            else
            {
                System.out.println(i + " is an odd number.");
            }

        }
    }
}