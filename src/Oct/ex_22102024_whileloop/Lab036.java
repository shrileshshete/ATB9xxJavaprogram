package Oct.ex_22102024_whileloop;

public class Lab036 {
    public static void main(String[] args) {
        int i = 1 ;
        while (i<=10)
        {
            if(i % 2 == 0)
            {
            System.out.println(i+ "Even No");
            }
            else
            {
            System.out.println(i+ "Odd No");
            }
            i++;
        }
    }
}
